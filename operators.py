from Datatypes import *

def _plus(arg1, arg2):
    return NumType(arg1 + arg2)

def _minus(arg1, arg2):
    return NumType(arg1 - arg2)

def _divide(arg1, arg2):
    return NumType(arg1 / arg2)

def _times(arg1, arg2):
    return NumType(arg1 * arg2)

def _power(arg1, arg2):
    return NumType(arg1 ** arg2)